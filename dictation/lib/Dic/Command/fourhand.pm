use strict;
use warnings;

use Dic -command;
sub usage_desc { "dic fourhand -c CTEST -t TOPIC -s STORY -f FORM" }

sub opt_spec  {
	return (
		["c=s", "cloze_style"]
		, ["t=s", "topic"]
		, ["s=s", "story"]
		, ["f=i", "form"]
	);
}

